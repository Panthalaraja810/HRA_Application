package Day5;

public class Pattern1 {

	
			//* * * * *
			//* * * * *
			//* * * * *
			//* * * * *
			//* * * * *
	
	public static void main(String[] args)
	{
		
		int n=5;
		for(int i=1; i<=n; i++) //rows
		{
			for(int j=1; j<=n; j++) //columns
			{
				System.out.print("* "); //printing statement
			}
			System.out.println(); //new line
		}
		
		
		
		
		
	}

}
